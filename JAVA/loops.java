public class loops {
    public static void main(String[] args) {
        
        // for loops
        for(int i=1;i<5;i++){
            System.out.println("count is "+i);
        }
        System.out.println("");

        // while loops
        int count=1;
        while(count!=6){
            System.out.println("Count is "+count);
            count++;
        }
        System.out.println("");

        // do while
        int counttt=1;
        do{
            System.out.println("Count value was"+counttt);
            counttt++;
        } while(counttt !=6);
    }
}
